package fr.arks.exiledarkanoid.gamephysics.bonus;

/**
 * Enum for the different bonus effects
 * <p>
 * NO_BLOCK_COLLISION: The ball will not collide with the blocks
 * PLATFORM_ENLARGE: The platform will be enlarged
 * PLATFORM_SHRINK: The platform will be shrinked
 * SCORE_BONUS: The player will get a score bonus
 * LIFE_BONUS: The player will get a life bonus
 * +
 */
public enum EBonusEffect {
    NO_BLOCK_COLLISION,
    PLATFORM_ENLARGE,
    PLATFORM_SHRINK,
    SCORE_BONUS,
    LIFE_BONUS,
}
